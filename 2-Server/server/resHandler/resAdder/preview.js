function addPreview(res, method) {
    // console.log('@@@',method)
    const data = []
    const size = s[method]
    let proc = []
    let tmp = p[method].split(' ')
    let tmpStr = ''
    for(let i = 0 ; i < tmp.length ; i ++) {
        tmpStr += tmp[i]
        if((i + 1) % c[method] === 0) {
            proc.push(tmpStr)
            tmpStr = ''
        }
        else {
            tmpStr += ', '
        }
    }

    for (let i = 0; i < size; i++) {
        data.push({
            key: i,
            nodeId: `节点 ${i}`,
            coreNum: c[method],
            process: proc[i],
        });
    }
    // console.log(data)
    res.resultPreview = data
}

module.exports = addPreview

